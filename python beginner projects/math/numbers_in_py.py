print(10 + 3)  # Addition
print(10 - 3)  # Subtraction
print(10 * 3)  # Multiplication
print(10 / 3)  # Division
print(10 // 3)  # Floor Division
print(10 % 3)  # Modulus
print(10 ** 3)  # Exponentiation
print(abs(-10))  # Absolute value
print(round(2.9))  # Rounding to the nearest integer
print(10 + 3 * 2 - 5)  # Order of operations (Multiplication before Addition)
print((10 + 3) * 2)  # Parentheses to change order of operations

x = 10
x = x + 3  # Incrementing x by 3
x += 3  # Another way to increment x by 3
print(x)  # Output the value of x after incrementing
