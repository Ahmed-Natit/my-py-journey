# Iterable
# An iterable is an object that can return its members one at a time, allowing it to be iterated over in a loop.

for x in "python":
 # For each value (letter) in the string 'python', let x be that value one at a time, and then do something with it (in this case, print it).
    print(x)
for y in [1, 2, 3, 4]:
    print(y)

shopping_car = ["apple", "banana", "orange"]

for item in shopping_car:
    print(item)
